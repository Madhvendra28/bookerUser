package com.model;

public class TextLink {
    private String requirement_link_id;
    private String text;
    private String post_link;

    public String getRequirement_link_id() {
        return requirement_link_id;
    }

    public void setRequirement_link_id(String requirement_link_id) {
        this.requirement_link_id = requirement_link_id;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public String getPost_link() {
        return post_link;
    }

    public void setPost_link(String post_link) {
        this.post_link = post_link;
    }
}
